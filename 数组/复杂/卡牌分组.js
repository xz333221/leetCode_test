var hasGroupsSizeX = function (deck) {
  let map = new Map()
  for (let i = 0; i < deck.length; i++) {
    if (map.get(deck[i])) {
      map.set(deck[i], map.get(deck[i]) + 1)
    } else {
      map.set(deck[i], 1)
    }
  }

  // 个数的集合
  let arr = Array.from(map.values())
  let num_arr = 0
  // 找到每组最少是多少
  let num_min = arr[0]
  for (let i = 0; i < arr.length; i++) {
    num_min = Math.min(num_min, arr[i])
  }
  console.log('num_min:',num_min)
  if (num_min < 2) return false
  // 找最小数的所有公约数
  let num_min_g_arr = []
  for (let i = 2; i <= num_min; i++) {
    if(num_min%i===0){
      num_min_g_arr.push(i)
    }
  }
  for (let i = 0; i < num_min_g_arr.length; ) {
    console.log('i:',i)
    for (let j = 0; j < arr.length; j++) {
      console.log('j:',j)
      if(arr[j] % num_min_g_arr[i] !== 0){
        i++
        break
      }else if(j===arr.length-1){
        num_arr ++
        return  true
      }
    }
  }
  console.log('num_min_g_arr:',num_min_g_arr)
  console.log('arr:',arr)
  return num_arr >= 1
}
